function processSensorData(csvData) {
    // 1. Parse the input CSV data
    const lines = csvData.trim().split('\n');
    const events = lines.map(line => {
        const [timestamp, patientID, event, sensorName] = line.split(',');
        return {
            timestamp: parseInt(timestamp, 10),
            patientID,
            event,
            sensorName: sensorName.trim() // Remove any extra whitespace
        };
    });

    // Get all unique timestamps and sort them
    const allTimestamps = [...new Set(events.map(e => e.timestamp))].sort((a, b) => a - b);

    // 2. Initialize data structures
    const nodeCountsByTimestamp = {};
    const nodeNames = ["Node #1", "Node #2", "Node #3"];

    // Initialize counts for each timestamp
    allTimestamps.forEach(ts => {
        nodeCountsByTimestamp[ts] = { "Node #1": 0, "Node #2": 0, "Node #3": 0 };
    });

    // 3. Process events to calculate patient counts
    const patientLocations = {}; // Track which sensor each patient is in

    events.forEach(({ timestamp, patientID, event, sensorName }) => {
        if (!nodeNames.includes(sensorName)) return; // Ignore irrelevant sensors

        const patientKey = patientID;

        if (event === 'enter') {
            // If patient was already in another sensor, they "left" it
            if (patientLocations[patientKey] && patientLocations[patientKey] !== sensorName) {
                const oldSensor = patientLocations[patientKey];
                // Decrement count for all future timestamps for the old sensor
                allTimestamps.filter(ts => ts >= timestamp).forEach(ts => {
                    if (nodeCountsByTimestamp[ts][oldSensor] > 0) {
                        nodeCountsByTimestamp[ts][oldSensor]--;
                    }
                });
            }
            
            // Increment count for all future timestamps for the new sensor
            allTimestamps.filter(ts => ts >= timestamp).forEach(ts => {
                nodeCountsByTimestamp[ts][sensorName]++;
            });
            patientLocations[patientKey] = sensorName;

        } else if (event === 'leave') {
            // Decrement count for all future timestamps
            allTimestamps.filter(ts => ts >= timestamp).forEach(ts => {
                if (nodeCountsByTimestamp[ts][sensorName] > 0) {
                    nodeCountsByTimestamp[ts][sensorName]--;
                }
            });
            if (patientLocations[patientKey] === sensorName) {
                delete patientLocations[patientKey];
            }
        }
    });

    // 4. Format the output CSV
    let outputCsv = "timestamp,patientsAtNode1,patientsAtNode2,patientsAtNode3\n";

    for (const ts of allTimestamps) {
        const counts = nodeCountsByTimestamp[ts];
        outputCsv += `${ts},${counts["Node #1"]},${counts["Node #2"]},${counts["Node #3"]}\n`;
    }

    return outputCsv;
}

