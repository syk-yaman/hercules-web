function processSensorData(csvData) {
    const lines = csvData.trim().split('\n');
    const events = lines.map(line => {
        const [timestamp, patientID, event, sensorName] = line.split(',');
        return {
            timestamp: parseInt(timestamp, 10),
            patientID,
            event,
            sensorName: sensorName.trim()
        };
    });

    const allTimestamps = [...new Set(events.map(e => e.timestamp))].sort((a, b) => a - b);
    const nodeNames = ["Node #1", "Node #2", "Node #3"];
    const nodeCountsByTimestamp = {};

    allTimestamps.forEach(ts => {
        nodeCountsByTimestamp[ts] = { "Node #1": 0, "Node #2": 0, "Node #3": 0 };
    });

    const patientLocations = {};

    events.forEach(({ timestamp, patientID, event, sensorName }) => {
        if (!nodeNames.includes(sensorName)) return;

        if (event === 'enter') {
            if (patientLocations[patientID] && patientLocations[patientID] !== sensorName) {
                const oldSensor = patientLocations[patientID];
                allTimestamps.filter(ts => ts >= timestamp).forEach(ts => {
                    if (nodeCountsByTimestamp[ts][oldSensor] > 0)
                        nodeCountsByTimestamp[ts][oldSensor]--;
                });
            }
            allTimestamps.filter(ts => ts >= timestamp).forEach(ts => {
                nodeCountsByTimestamp[ts][sensorName]++;
            });
            patientLocations[patientID] = sensorName;

        } else if (event === 'leave') {
            allTimestamps.filter(ts => ts >= timestamp).forEach(ts => {
                if (nodeCountsByTimestamp[ts][sensorName] > 0)
                    nodeCountsByTimestamp[ts][sensorName]--;
            });
            if (patientLocations[patientID] === sensorName)
                delete patientLocations[patientID];
        }
    });

    const transform = count => (count * 1.5 + 6.27).toFixed(2);

    let outputCsv = "timestamp,patientsAtNode1,patientsAtNode2,patientsAtNode3\n";
    for (const ts of allTimestamps) {
        const counts = nodeCountsByTimestamp[ts];
        outputCsv += `${ts},${transform(counts["Node #1"])},${transform(counts["Node #2"])},${transform(counts["Node #3"])}\n`;
    }

    return outputCsv;
}

